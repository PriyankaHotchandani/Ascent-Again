      <h2 class="heading font-x3">Pension For Widows</h2>
      <p><br><br> Gandhi National Widow Pension Scheme (IGNWPS) was launched by the Central Government in 1995 as a part
        of the National Social Assistance programme. The objective of the scheme is to empower the widows of the state.

        The widow women between the age of 18 to 60 may registered under the Delhi Vidhwa Pension Yojana and may avail
        the benefits of the scheme. Government of Delhi will provide financial relief of INR 2500 per month to the
        selected beneficiaries under the Delhi Widow Pension Yojana. Delhi Widow Pension scheme Application form 2019 is
        available online.
      </p><br>
      <h4>Application Procedure for the Scheme</h4>
      <p>The first step, in this case, is to obtain the application form. The form can be obtained from the district
        office or it can also be downloaded from the online source. The form is available on the website of the
        department of women and child development.
        Once you have downloaded the form, you would then require filling the form and you would also need three of your
        passport size photographs.
        It is also required to provide the details of the bank account in your form as the benefits will directly be
        transferred to your bank account. This falls under the direct benefit transfer initiative of the government.
        The form then needs to be signed by the women and this should be submitted to the Department of Women and Child
        Development.
        Link to the website of the Department of Women and Child Development:
        <br><br><a class="btn" href="https://wcd.nic.in/">Read
          More</a><br>
      </p><br>
      <h4>Following are the documents for availing the benefits of the scheme</h4>
      <p>Age Proof,
        Residential Proof,
        Xerox of Bank Passbook,
        In case of any disability, the women would also require submitting a disability certificate;
        Death certificate of Husband's Death.
        In the case of any mental illness, a certificate for the same should be submitted. Once the form has been
        submitted, it will be sent for verification and upon successful verification, the pension will be started.
